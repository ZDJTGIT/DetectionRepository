package com.zhongda.detection.web.model;

public class FarmlandThreshold {
    private Integer farmlandThresholdId;

    private Float maxPh;

    private Float minPh;

    private Float maxDepth;

    private Float minDepth;

    private Float maxTotalCadmium;

    private Float minTotalCadmium;

    private Float maxEffectiveCadmium;

    private Float minEffectiveCadmium;

    public Integer getFarmlandThresholdId() {
        return farmlandThresholdId;
    }

    public void setFarmlandThresholdId(Integer farmlandThresholdId) {
        this.farmlandThresholdId = farmlandThresholdId;
    }

    public Float getMaxPh() {
        return maxPh;
    }

    public void setMaxPh(Float maxPh) {
        this.maxPh = maxPh;
    }

    public Float getMinPh() {
        return minPh;
    }

    public void setMinPh(Float minPh) {
        this.minPh = minPh;
    }

    public Float getMaxDepth() {
        return maxDepth;
    }

    public void setMaxDepth(Float maxDepth) {
        this.maxDepth = maxDepth;
    }

    public Float getMinDepth() {
        return minDepth;
    }

    public void setMinDepth(Float minDepth) {
        this.minDepth = minDepth;
    }

    public Float getMaxTotalCadmium() {
        return maxTotalCadmium;
    }

    public void setMaxTotalCadmium(Float maxTotalCadmium) {
        this.maxTotalCadmium = maxTotalCadmium;
    }

    public Float getMinTotalCadmium() {
        return minTotalCadmium;
    }

    public void setMinTotalCadmium(Float minTotalCadmium) {
        this.minTotalCadmium = minTotalCadmium;
    }

    public Float getMaxEffectiveCadmium() {
        return maxEffectiveCadmium;
    }

    public void setMaxEffectiveCadmium(Float maxEffectiveCadmium) {
        this.maxEffectiveCadmium = maxEffectiveCadmium;
    }

    public Float getMinEffectiveCadmium() {
        return minEffectiveCadmium;
    }

    public void setMinEffectiveCadmium(Float minEffectiveCadmium) {
        this.minEffectiveCadmium = minEffectiveCadmium;
    }
}