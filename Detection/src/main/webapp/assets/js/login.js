
$(document).ready(function(){
	
		$("#name").focus(function(){
			$(this).css({"background-color":"#FFFFFF"});
		});
		$("#name").blur(function(){
			$(this).css("background-color","#F3F2EE");
		});
		$("#password").focus(function(){
			$(this).css({"background-color":"#FFFFFF"});
		});
		$("#password").blur(function(){
			$(this).css("background-color","#F3F2EE");
		});
		
	
});



	
	
